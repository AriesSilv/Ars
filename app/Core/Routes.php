<?php

namespace Ars\Core;

use Ars\Config\App;
use ReflectionMethod;

class Routes {

    protected $config;

    protected $controller;

    protected $method;

    protected $params = [];

    /*
    |--------------------------------------------------------------------------
    | CONSTRUCT
    |--------------------------------------------------------------------------
    */

    public function __construct(){

        $this->routing();
    }

    /*
    |--------------------------------------------------------------------------
    | ROUTING
    |--------------------------------------------------------------------------
    */

    protected function routing(){

        /*
        |--------------------------------------------------------------------------
        | CONFIG
        |--------------------------------------------------------------------------
        */

        $this->config =
            new App;

        /*
        |--------------------------------------------------------------------------
        | DEFAULT
        |--------------------------------------------------------------------------
        */

        $this->controller =
            $this->config->default_controller;

        $this->method =
            $this->config->default_method;

        /*
        |--------------------------------------------------------------------------
        | URL
        |--------------------------------------------------------------------------
        */

        $url =
            $this->parseURL();

        /*
        |--------------------------------------------------------------------------
        | CONTROLLER
        |--------------------------------------------------------------------------
        */

        if (isset($url[0])) {

            $controller =
                ucfirst($url[0]);

            $controllerClass =
                "Ars\\Controllers\\"
                . $controller;

            if (
                class_exists(
                    $controllerClass
                )
            ) {

                $this->controller =
                    $controller;

                unset($url[0]);
            }
        }

        /*
        |--------------------------------------------------------------------------
        | CONTROLLER CLASS
        |--------------------------------------------------------------------------
        */

        $controllerClass =
            "Ars\\Controllers\\"
            . $this->controller;

        /*
        |--------------------------------------------------------------------------
        | CONTROLLER EXISTS
        |--------------------------------------------------------------------------
        */

        if (
            !class_exists(
                $controllerClass
            )
        ) {

            $this->error404();
        }

        /*
        |--------------------------------------------------------------------------
        | INSTANCE CONTROLLER
        |--------------------------------------------------------------------------
        */

        $this->controller =
            new $controllerClass;

        /*
        |--------------------------------------------------------------------------
        | METHOD
        |--------------------------------------------------------------------------
        */

        if (isset($url[1])) {

            if (
                method_exists(
                    $this->controller,
                    $url[1]
                )
            ) {

                $reflection =
                    new ReflectionMethod(
                        $this->controller,
                        $url[1]
                    );

                if (
                    $reflection
                        ->isPublic()
                ) {

                    $this->method =
                        $url[1];

                    unset($url[1]);
                }
            }
        }

        /*
        |--------------------------------------------------------------------------
        | PARAMS
        |--------------------------------------------------------------------------
        */

        $this->params =
            !empty($url)
            ? array_values($url)
            : [];

        /*
        |--------------------------------------------------------------------------
        | CALL
        |--------------------------------------------------------------------------
        */

        call_user_func_array(
            [
                $this->controller,
                $this->method
            ],
            $this->params
        );
    }

    /*
    |--------------------------------------------------------------------------
    | PARSE URL
    |--------------------------------------------------------------------------
    */

    protected function parseURL(){

        if (
            isset($_GET['url'])
            &&
            !empty($_GET['url'])
        ) {

            $url =
                rtrim(
                    $_GET['url'],
                    '/'
                );

            $url =
                filter_var(
                    $url,
                    FILTER_SANITIZE_URL
                );

            return
                explode('/', $url);
        }

        return [];
    }

    /*
    |--------------------------------------------------------------------------
    | ERROR 404
    |--------------------------------------------------------------------------
    */

    protected function error404(){

        http_response_code(404);

        die('404 Not Found');
    }
}