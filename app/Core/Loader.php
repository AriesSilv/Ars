<?php

namespace Ars\Core;

class Loader {

    protected $controller;

    protected $library_loaded = [];

    protected $model_loaded = [];

    protected $helper_loaded = [];

    protected $config_loaded = [];

    protected $database_loaded = [];

    /*
    |--------------------------------------------------------------------------
    | CONSTRUCT
    |--------------------------------------------------------------------------
    */

    public function __construct(
        $controller = null
    ){

        $this->controller =
            $controller;
    }

    /*
    |--------------------------------------------------------------------------
    | VIEW
    |--------------------------------------------------------------------------
    */

    public function view(
        $view,
        $data = [],
        $return = false
    ){

        extract(
            $data,
            EXTR_SKIP
        );

        $file =
            "app/views/"
            . $view
            . ".php";

        if (!file_exists($file)) {

            throw new \Exception(
                "View '$view' not found"
            );
        }

        /*
        |--------------------------------------------------------------------------
        | RETURN
        |--------------------------------------------------------------------------
        */

        if ($return) {

            ob_start();

            require $file;

            return ob_get_clean();
        }

        /*
        |--------------------------------------------------------------------------
        | NORMAL
        |--------------------------------------------------------------------------
        */

        require $file;
    }

    /*
    |--------------------------------------------------------------------------
    | MODEL
    |--------------------------------------------------------------------------
    */

    public function model($model){

        $property =
            strtolower($model)
            . "_model";

        /*
        |--------------------------------------------------------------------------
        | CACHE
        |--------------------------------------------------------------------------
        */

        if (
            isset(
                $this->model_loaded[$model]
            )
        ) {

            $instance =
                $this->model_loaded[$model];

            if ($this->controller) {

                $this->controller->$property =
                    $instance;
            }

            return $instance;
        }

        /*
        |--------------------------------------------------------------------------
        | CLASS
        |--------------------------------------------------------------------------
        */

        $class =
            "\\Ars\\Models\\{$model}";

        if (!class_exists($class)) {

            throw new \Exception(
                "Model '$class' not found"
            );
        }

        /*
        |--------------------------------------------------------------------------
        | INSTANCE
        |--------------------------------------------------------------------------
        */

        $instance =
            new $class;

        /*
        |--------------------------------------------------------------------------
        | AUTO INJECT
        |--------------------------------------------------------------------------
        */

        if ($this->controller) {

            $this->controller->$property =
                $instance;
        }

        /*
        |--------------------------------------------------------------------------
        | CACHE SAVE
        |--------------------------------------------------------------------------
        */

        $this->model_loaded[$model] =
            $instance;

        return $instance;
    }

    /*
    |--------------------------------------------------------------------------
    | LIBRARY
    |--------------------------------------------------------------------------
    */

    public function library($library){

        $property =
            strtolower($library);

        /*
        |--------------------------------------------------------------------------
        | CACHE
        |--------------------------------------------------------------------------
        */

        if (
            isset(
                $this->library_loaded[$property]
            )
        ) {

            $instance =
                $this->library_loaded[$property];

            if ($this->controller) {

                $this->controller->$property =
                    $instance;
            }

            return $instance;
        }

        /*
        |--------------------------------------------------------------------------
        | CLASS
        |--------------------------------------------------------------------------
        */

        $class =
            "\\Ars\\Libraries\\{$library}";

        if (!class_exists($class)) {

            throw new \Exception(
                "Library '$class' not found"
            );
        }

        /*
        |--------------------------------------------------------------------------
        | INSTANCE
        |--------------------------------------------------------------------------
        */

        $instance =
            new $class;

        /*
        |--------------------------------------------------------------------------
        | AUTO INJECT
        |--------------------------------------------------------------------------
        */

        if ($this->controller) {

            $this->controller->$property =
                $instance;
        }

        /*
        |--------------------------------------------------------------------------
        | CACHE SAVE
        |--------------------------------------------------------------------------
        */

        $this->library_loaded[$property] =
            $instance;

        return $instance;
    }

    /*
    |--------------------------------------------------------------------------
    | HELPER
    |--------------------------------------------------------------------------
    */

    public function helper($helper){

        if (
            isset(
                $this->helper_loaded[$helper]
            )
        ) {

            return true;
        }

        /*
        |--------------------------------------------------------------------------
        | FILE
        |--------------------------------------------------------------------------
        */

        $file =
            "app/helpers/"
            . $helper
            . "_helper.php";

        if (!file_exists($file)) {

            throw new \Exception(
                "Helper '$helper' not found"
            );
        }

        require_once $file;

        /*
        |--------------------------------------------------------------------------
        | CACHE SAVE
        |--------------------------------------------------------------------------
        */

        $this->helper_loaded[$helper] =
            true;

        return true;
    }

    /*
    |--------------------------------------------------------------------------
    | CONFIG
    |--------------------------------------------------------------------------
    */

    public function config($config){

        $property =
            strtolower($config);

        /*
        |--------------------------------------------------------------------------
        | CACHE
        |--------------------------------------------------------------------------
        */

        if (
            isset(
                $this->config_loaded[$property]
            )
        ) {

            return
                $this->config_loaded[$property];
        }

        /*
        |--------------------------------------------------------------------------
        | CLASS
        |--------------------------------------------------------------------------
        */

        $class =
            "\\Ars\\Config\\{$config}";

        if (!class_exists($class)) {

            throw new \Exception(
                "Config '$class' not found"
            );
        }

        /*
        |--------------------------------------------------------------------------
        | INSTANCE
        |--------------------------------------------------------------------------
        */

        $instance =
            new $class;

        /*
        |--------------------------------------------------------------------------
        | CACHE SAVE
        |--------------------------------------------------------------------------
        */

        $this->config_loaded[$property] =
            $instance;

        return $instance;
    }

    /*
    |--------------------------------------------------------------------------
    | DATABASE
    |--------------------------------------------------------------------------
    */

    public function database(
        $key = null
    ){

        /*
        |--------------------------------------------------------------------------
        | CONFIG
        |--------------------------------------------------------------------------
        */

        $config =
            $this->config("Database");

        /*
        |--------------------------------------------------------------------------
        | DEFAULT CONNECTION
        |--------------------------------------------------------------------------
        */

        if (empty($key)) {

            $key =
                $config->default_connection;
        }

        /*
        |--------------------------------------------------------------------------
        | CONNECTION EXISTS
        |--------------------------------------------------------------------------
        */

        if (
            !isset(
                $config->connections[$key]
            )
        ) {

            throw new \Exception(
                "Database connection '$key' not found"
            );
        }

        /*
        |--------------------------------------------------------------------------
        | PROPERTY
        |--------------------------------------------------------------------------
        */

        $property =
            $key ===
            $config->default_connection
            ? "db"
            : "db_" . strtolower($key);

        /*
        |--------------------------------------------------------------------------
        | CACHE
        |--------------------------------------------------------------------------
        */

        if (
            isset(
                $this->database_loaded[$key]
            )
        ) {

            $db =
                $this->database_loaded[$key];

        } else {

            /*
            |--------------------------------------------------------------------------
            | CLASS
            |--------------------------------------------------------------------------
            */

            $class =
                "\\Ars\\Libraries\\Database\\Database";

            if (!class_exists($class)) {

                throw new \Exception(
                    "Library Database '$class' not found"
                );
            }

            /*
            |--------------------------------------------------------------------------
            | INSTANCE
            |--------------------------------------------------------------------------
            */

            $db =
                new $class($key);

            /*
            |--------------------------------------------------------------------------
            | CACHE SAVE
            |--------------------------------------------------------------------------
            */

            $this->database_loaded[$key] =
                $db;
        }

        /*
        |--------------------------------------------------------------------------
        | AUTO INJECT
        |--------------------------------------------------------------------------
        */

        if ($this->controller) {

            $this->controller->$property =
                $db;
        }

        return $db;
    }
}