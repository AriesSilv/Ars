<?php
namespace Ars\Libraries\Database\QB;
trait Get {

    /*
    |--------------------------------------------------------------------------
    | GET
    |--------------------------------------------------------------------------
    */

    public function get(
        $table = '',
        $limit = null,
        $offset = null
    ){

        if ($table) {

            $this->table = $table;
        }

        /*
        |--------------------------------------------------------------------------
        | LIMIT
        |--------------------------------------------------------------------------
        */

        if ($limit !== null) {

            $this->limit(
                $limit,
                $offset ?? 0
            );
        }

        /*
        |--------------------------------------------------------------------------
        | SQL
        |--------------------------------------------------------------------------
        */

        $sql =
            $this->get_compiled_select(
                $this->table,
                false
            );

        /*
        |--------------------------------------------------------------------------
        | QUERY
        |--------------------------------------------------------------------------
        */

        $this->query($sql);

        /*
        |--------------------------------------------------------------------------
        | BINDINGS
        |--------------------------------------------------------------------------
        */

        foreach ($this->bindings as $k => $v) {

            $this->bind($k, $v);
        }

        /*
        |--------------------------------------------------------------------------
        | EXECUTE
        |--------------------------------------------------------------------------
        */

        $this->execute();

        /*
        |--------------------------------------------------------------------------
        | RESULT
        |--------------------------------------------------------------------------
        */

        $result =
            $this->result();

        /*
        |--------------------------------------------------------------------------
        | CLEANUP
        |--------------------------------------------------------------------------
        */

        $this->free_result();

        $this->resetQB();

        return $result;
    }

    /*
    |--------------------------------------------------------------------------
    | GET WHERE
    |--------------------------------------------------------------------------
    */

    public function get_where(
        $table = '',
        $where = [],
        $limit = null,
        $offset = null
    ){

        if ($table) {

            $this->table = $table;
        }

        /*
        |--------------------------------------------------------------------------
        | WHERE
        |--------------------------------------------------------------------------
        */

        if (!empty($where)) {

            $this->where($where);
        }

        return $this->get(
            $this->table,
            $limit,
            $offset
        );
    }

    /*
    |--------------------------------------------------------------------------
    | COUNT ALL RESULTS
    |--------------------------------------------------------------------------
    */

    public function count_all_results(
        $table = '',
        $reset = true
    ){

        if ($table) {

            $this->table = $table;
        }

        /*
        |--------------------------------------------------------------------------
        | OVERRIDE SELECT
        |--------------------------------------------------------------------------
        */

        $this->select =
            "COUNT(*) AS total";

        /*
        |--------------------------------------------------------------------------
        | SQL
        |--------------------------------------------------------------------------
        */

        $sql =
            $this->get_compiled_select(
                $this->table,
                false
            );

        /*
        |--------------------------------------------------------------------------
        | QUERY
        |--------------------------------------------------------------------------
        */

        $this->query($sql);

        /*
        |--------------------------------------------------------------------------
        | BIND
        |--------------------------------------------------------------------------
        */

        foreach ($this->bindings as $k => $v) {

            $this->bind($k, $v);
        }

        /*
        |--------------------------------------------------------------------------
        | EXECUTE
        |--------------------------------------------------------------------------
        */

        $this->execute();

        /*
        |--------------------------------------------------------------------------
        | RESULT
        |--------------------------------------------------------------------------
        */

        $row =
            $this->row_array();

        /*
        |--------------------------------------------------------------------------
        | CLEANUP
        |--------------------------------------------------------------------------
        */

        $this->free_result();

        if ($reset) {

            $this->resetQB();
        }

        return (int)
            ($row['total'] ?? 0);
    }

}