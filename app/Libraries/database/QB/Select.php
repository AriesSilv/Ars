<?php
namespace Ars\Libraries\Database\QB;

trait Select {

    /*
    |--------------------------------------------------------------------------
    | SELECT
    |--------------------------------------------------------------------------
    */

    public function select(
        $select = '*',
        $escape = null
    ){

        /*
        |--------------------------------------------------------------------------
        | ARRAY
        |--------------------------------------------------------------------------
        */

        if (is_array($select)) {

            foreach ($select as $field) {

                $field = trim($field);

                if ($field === '') {
                    continue;
                }

                $this->select[] = $field;
            }

        } else {

            $select = trim($select);

            if ($select !== '') {

                /*
                |--------------------------------------------------------------------------
                | MULTI STRING
                |--------------------------------------------------------------------------
                */

                if (str_contains($select, ',')) {

                    $parts =
                        array_map(
                            'trim',
                            explode(',', $select)
                        );

                    foreach ($parts as $field) {

                        if ($field === '') {
                            continue;
                        }

                        $this->select[] =
                            $field;
                    }

                } else {

                    $this->select[] =
                        $select;
                }
            }
        }

        /*
        |--------------------------------------------------------------------------
        | REMOVE DEFAULT *
        |--------------------------------------------------------------------------
        */

        if (
            count($this->select) > 1
            && in_array('*', $this->select)
        ) {

            $this->select =
                array_values(
                    array_filter(
                        $this->select,
                        fn($v) => $v !== '*'
                    )
                );
        }

        /*
        |--------------------------------------------------------------------------
        | UNIQUE
        |--------------------------------------------------------------------------
        */

        $this->select =
            array_values(
                array_unique($this->select)
            );

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | SELECT FUNCTION
    |--------------------------------------------------------------------------
    */

    private function select_func(
        $func,
        $field,
        $alias = ''
    ){

        $func =
            strtoupper(
                trim($func)
            );

        $alias =
            trim(
                $alias ?: "{$field}_{$func}"
            );

        /*
        |--------------------------------------------------------------------------
        | REMOVE DEFAULT *
        |--------------------------------------------------------------------------
        */

        if (
            count($this->select) === 1
            && $this->select[0] === '*'
        ) {

            $this->select = [];
        }

        /*
        |--------------------------------------------------------------------------
        | FIELD
        |--------------------------------------------------------------------------
        */

        $fieldProtected =
            $this->protect_identifier(
                $field
            );

        /*
        |--------------------------------------------------------------------------
        | SELECT
        |--------------------------------------------------------------------------
        */

        $this->select[] =
            "{$func}({$fieldProtected}) AS `{$alias}`";

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | SELECT AVG
    |--------------------------------------------------------------------------
    */

    public function select_avg(
        $field,
        $alias = ''
    ){

        return $this->select_func(
            'AVG',
            $field,
            $alias
        );
    }

    /*
    |--------------------------------------------------------------------------
    | SELECT MAX
    |--------------------------------------------------------------------------
    */

    public function select_max(
        $field,
        $alias = ''
    ){

        return $this->select_func(
            'MAX',
            $field,
            $alias
        );
    }

    /*
    |--------------------------------------------------------------------------
    | SELECT MIN
    |--------------------------------------------------------------------------
    */

    public function select_min(
        $field,
        $alias = ''
    ){

        return $this->select_func(
            'MIN',
            $field,
            $alias
        );
    }

    /*
    |--------------------------------------------------------------------------
    | SELECT SUM
    |--------------------------------------------------------------------------
    */

    public function select_sum(
        $field,
        $alias = ''
    ){

        return $this->select_func(
            'SUM',
            $field,
            $alias
        );
    }

    /*
    |--------------------------------------------------------------------------
    | DISTINCT
    |--------------------------------------------------------------------------
    */

    public function distinct(
        $val = true
    ){

        $this->distinct =
            (bool) $val;

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | TABLE
    |--------------------------------------------------------------------------
    */

    public function table($table){

        $this->table =
            trim($table);

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | GROUP BY
    |--------------------------------------------------------------------------
    */

    public function group_by($group){

        if (is_array($group)) {

            foreach ($group as $g) {

                $g = trim($g);

                if ($g === '') {
                    continue;
                }

                $this->groupBy[] = $g;
            }

        } else {

            $group = trim($group);

            if ($group !== '') {

                /*
                |--------------------------------------------------------------------------
                | MULTI GROUP
                |--------------------------------------------------------------------------
                */

                if (str_contains($group, ',')) {

                    $parts =
                        array_map(
                            'trim',
                            explode(',', $group)
                        );

                    foreach ($parts as $g) {

                        if ($g === '') {
                            continue;
                        }

                        $this->groupBy[] = $g;
                    }

                } else {

                    $this->groupBy[] =
                        $group;
                }
            }
        }

        /*
        |--------------------------------------------------------------------------
        | UNIQUE
        |--------------------------------------------------------------------------
        */

        $this->groupBy =
            array_values(
                array_unique($this->groupBy)
            );

        return $this;
    }

}