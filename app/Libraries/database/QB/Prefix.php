<?php
namespace Ars\Libraries\Database\QB;

trait Prefix {

    /*
    |--------------------------------------------------------------------------
    | SET DB PREFIX
    |--------------------------------------------------------------------------
    */

    public function set_dbprefix(
        $prefix = ''
    ){

        $this->dbprefix =
            trim($prefix);

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | GET PREFIXED TABLE
    |--------------------------------------------------------------------------
    */

    public function dbprefix(
        $table = ''
    ){

        if (empty($table)) {

            return '';
        }

        $table = trim($table);

        /*
        |--------------------------------------------------------------------------
        | RAW SQL / ALIAS
        |--------------------------------------------------------------------------
        */

        if (
            str_contains($table, ' ')
            || str_contains($table, '(')
            || str_contains($table, ')')
        ) {

            return $table;
        }

        /*
        |--------------------------------------------------------------------------
        | NO PREFIX
        |--------------------------------------------------------------------------
        */

        if (empty($this->dbprefix)) {

            return $table;
        }

        /*
        |--------------------------------------------------------------------------
        | ALREADY PREFIXED
        |--------------------------------------------------------------------------
        */

        if (
            str_starts_with(
                $table,
                $this->dbprefix
            )
        ) {

            return $table;
        }

        /*
        |--------------------------------------------------------------------------
        | DOT TABLE
        |--------------------------------------------------------------------------
        */

        if (str_contains($table, '.')) {

            $parts =
                explode('.', $table);

            $last =
                array_pop($parts);

            $last =
                $this->dbprefix . $last;

            $parts[] = $last;

            return implode('.', $parts);
        }

        /*
        |--------------------------------------------------------------------------
        | RETURN PREFIX
        |--------------------------------------------------------------------------
        */

        return
            $this->dbprefix
            . $table;
    }

}