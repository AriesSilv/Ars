<?php
namespace Ars\Libraries\Database\QB;
trait Prefix {

    /*
    |--------------------------------------------------------------------------
    | SET DB PREFIX
    |--------------------------------------------------------------------------
    */

    public function set_dbprefix(
        $prefix = ''
    ){

        $this->dbprefix =
            trim($prefix);

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | APPLY PREFIX
    |--------------------------------------------------------------------------
    */

    public function dbprefix(
        $table = ''
    ){

        if (empty($table)) {
            return '';
        }

        /*
        |--------------------------------------------------------------------------
        | ALREADY PREFIXED
        |--------------------------------------------------------------------------
        */

        if (
            !empty($this->dbprefix) &&
            strpos(
                $table,
                $this->dbprefix
            ) === 0
        ) {

            return $table;
        }

        /*
        |--------------------------------------------------------------------------
        | RETURN PREFIXED
        |--------------------------------------------------------------------------
        */

        return
            $this->dbprefix
            . $table;
    }

}