<?php
namespace Ars\Libraries\Database\QB;
trait Having {

    /*
    |--------------------------------------------------------------------------
    | HAVING
    |--------------------------------------------------------------------------
    */

    public function having(
        $key = null,
        $value = null,
        $escape = null
    ){

        return $this->buildHaving(
            $key,
            $value,
            'AND'
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR HAVING
    |--------------------------------------------------------------------------
    */

    public function or_having(
        $key = null,
        $value = null,
        $escape = null
    ){

        return $this->buildHaving(
            $key,
            $value,
            'OR'
        );
    }

    /*
    |--------------------------------------------------------------------------
    | BUILD HAVING
    |--------------------------------------------------------------------------
    */

    private function buildHaving(
        $key,
        $value,
        $boolean
    ){

        /*
        |--------------------------------------------------------------------------
        | ARRAY SUPPORT
        |--------------------------------------------------------------------------
        */

        if (is_array($key)) {

            foreach ($key as $k => $v) {

                $this->buildHaving(
                    $k,
                    $v,
                    $boolean
                );

            }

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | RAW CONDITION
        |--------------------------------------------------------------------------
        */

        if ($value === null) {

            $this->addHaving(
                $key,
                $boolean
            );

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | OPERATOR DETECT
        |--------------------------------------------------------------------------
        */

        if (
            preg_match(
                '/(>=|<=|!=|<>|>|<|LIKE)$/i',
                trim($key)
            )
        ) {

            $field = $key;

        } else {

            $field = "`{$key}` =";

        }

        /*
        |--------------------------------------------------------------------------
        | PARAM
        |--------------------------------------------------------------------------
        */

        $param =
            'having_' .
            str_replace(
                ['.', ' '],
                '_',
                $key
            ) .
            count($this->bindings);

        /*
        |--------------------------------------------------------------------------
        | CONDITION
        |--------------------------------------------------------------------------
        */

        $this->addHaving(
            "{$field} :{$param}",
            $boolean
        );

        $this->bindings[$param] = $value;

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | ADD HAVING
    |--------------------------------------------------------------------------
    */

    protected function addHaving(
        $condition,
        $boolean
    ){

        if (empty($this->having)) {

            $this->having[] = $condition;

        } else {

            $this->having[] =
                "{$boolean} {$condition}";
        }

        return $this;
    }

}