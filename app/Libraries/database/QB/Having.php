<?php
namespace Ars\Libraries\Database\QB;

trait Having {

    /*
    |--------------------------------------------------------------------------
    | HAVING
    |--------------------------------------------------------------------------
    */

    public function having(
        $key = null,
        $value = null,
        $escape = null
    ){

        return $this->buildHaving(
            $key,
            $value,
            'AND'
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR HAVING
    |--------------------------------------------------------------------------
    */

    public function or_having(
        $key = null,
        $value = null,
        $escape = null
    ){

        return $this->buildHaving(
            $key,
            $value,
            'OR'
        );
    }

    /*
    |--------------------------------------------------------------------------
    | BUILD HAVING
    |--------------------------------------------------------------------------
    */

    private function buildHaving(
        $key,
        $value,
        $boolean
    ){

        /*
        |--------------------------------------------------------------------------
        | ARRAY SUPPORT
        |--------------------------------------------------------------------------
        */

        if (is_array($key)) {

            foreach ($key as $k => $v) {

                $this->buildHaving(
                    $k,
                    $v,
                    $boolean
                );
            }

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | RAW CONDITION
        |--------------------------------------------------------------------------
        */

        if ($value === null) {

            $this->addHaving(
                $key,
                $boolean
            );

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | OPERATOR DETECT
        |--------------------------------------------------------------------------
        */

        $field =
            trim($key);

        $operator = '=';

        if (
            preg_match(
                '/^(.+?)\s*(>=|<=|!=|<>|>|<|LIKE)$/i',
                trim($key),
                $match
            )
        ) {

            $field =
                trim($match[1]);

            $operator =
                strtoupper(
                    trim($match[2])
                );
        }

        /*
        |--------------------------------------------------------------------------
        | FIELD
        |--------------------------------------------------------------------------
        */

        $fieldProtected =
            $this->protect_identifier(
                $field
            );

        /*
        |--------------------------------------------------------------------------
        | PARAM
        |--------------------------------------------------------------------------
        */

        $param =
            'having_' .
            preg_replace(
                '/[^a-zA-Z0-9_]/',
                '_',
                $field
            ) .
            count($this->bindings);

        /*
        |--------------------------------------------------------------------------
        | CONDITION
        |--------------------------------------------------------------------------
        */

        $condition =
            "{$fieldProtected} {$operator} :{$param}";

        $this->addHaving(
            $condition,
            $boolean
        );

        /*
        |--------------------------------------------------------------------------
        | BINDING
        |--------------------------------------------------------------------------
        */

        $this->bindings[$param] =
            $value;

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | ADD HAVING
    |--------------------------------------------------------------------------
    */

    protected function addHaving(
        $condition,
        $boolean
    ){

        /*
        |--------------------------------------------------------------------------
        | FIRST CONDITION
        |--------------------------------------------------------------------------
        */

        if (empty($this->having)) {

            $this->having[] =
                $condition;

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | NORMAL
        |--------------------------------------------------------------------------
        */

        $this->having[] =
            "{$boolean} {$condition}";

        return $this;
    }

}