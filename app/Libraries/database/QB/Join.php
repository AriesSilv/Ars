<?php
namespace Ars\Libraries\Database\QB;
trait Join {

    /*
    |--------------------------------------------------------------------------
    | JOIN
    |--------------------------------------------------------------------------
    */

    public function join(
        $table,
        $condition = '',
        $type = '',
        $escape = null
    ){

        /*
        |--------------------------------------------------------------------------
        | NORMALIZE TYPE
        |--------------------------------------------------------------------------
        */

        $type = strtoupper(
            trim($type)
        );

        if ($type == '') {

            $type = 'INNER';
        }

        /*
        |--------------------------------------------------------------------------
        | VALID JOIN TYPE
        |--------------------------------------------------------------------------
        */

        $allowed = [
            'INNER',
            'LEFT',
            'RIGHT',
            'LEFT OUTER',
            'RIGHT OUTER'
        ];

        if (!in_array($type, $allowed)) {

            $type = 'INNER';
        }

        /*
        |--------------------------------------------------------------------------
        | BUILD JOIN
        |--------------------------------------------------------------------------
        */

        $join =
            "{$type} JOIN `{$table}`";

        if (!empty($condition)) {

            $join .= " ON {$condition}";
        }

        $this->join[] = $join;

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | LEFT JOIN
    |--------------------------------------------------------------------------
    */

    public function left_join(
        $table,
        $condition
    ){

        return $this->join(
            $table,
            $condition,
            'LEFT'
        );
    }

    /*
    |--------------------------------------------------------------------------
    | RIGHT JOIN
    |--------------------------------------------------------------------------
    */

    public function right_join(
        $table,
        $condition
    ){

        return $this->join(
            $table,
            $condition,
            'RIGHT'
        );
    }

    /*
    |--------------------------------------------------------------------------
    | INNER JOIN
    |--------------------------------------------------------------------------
    */

    public function inner_join(
        $table,
        $condition
    ){

        return $this->join(
            $table,
            $condition,
            'INNER'
        );
    }

}