<?php
namespace Ars\Libraries\Database\QB;

trait Group {

    /*
    |--------------------------------------------------------------------------
    | INTERNAL GROUP HELPER
    |--------------------------------------------------------------------------
    */

    private function addGroup(
        $prefix = 'AND',
        $not = false
    ){

        /*
        |--------------------------------------------------------------------------
        | FIRST GROUP
        |--------------------------------------------------------------------------
        */

        if (empty($this->where)) {

            $condition =
                $not
                ? 'NOT ('
                : '(';

        } else {

            /*
            |--------------------------------------------------------------------------
            | AFTER OPEN GROUP
            |--------------------------------------------------------------------------
            */

            $last =
                end($this->where);

            if (
                $last === '('
                || $last === 'NOT ('
                || str_ends_with(
                    trim($last),
                    '('
                )
            ) {

                $condition =
                    $not
                    ? 'NOT ('
                    : '(';

            } else {

                $condition =
                    $not
                    ? "{$prefix} NOT ("
                    : "{$prefix} (";
            }
        }

        /*
        |--------------------------------------------------------------------------
        | PUSH GROUP
        |--------------------------------------------------------------------------
        */

        $this->where[] =
            trim($condition);

        $this->groupStack[] =
            '(';

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | AND GROUP
    |--------------------------------------------------------------------------
    */

    public function group_start(){

        return $this->addGroup(
            'AND',
            false
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR GROUP
    |--------------------------------------------------------------------------
    */

    public function or_group_start(){

        return $this->addGroup(
            'OR',
            false
        );
    }

    /*
    |--------------------------------------------------------------------------
    | NOT GROUP
    |--------------------------------------------------------------------------
    */

    public function not_group_start(){

        return $this->addGroup(
            'AND',
            true
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR NOT GROUP
    |--------------------------------------------------------------------------
    */

    public function or_not_group_start(){

        return $this->addGroup(
            'OR',
            true
        );
    }

    /*
    |--------------------------------------------------------------------------
    | GROUP END
    |--------------------------------------------------------------------------
    */

    public function group_end(){

        if (!empty($this->groupStack)) {

            $this->where[] = ')';

            array_pop($this->groupStack);
        }

        return $this;
    }

}