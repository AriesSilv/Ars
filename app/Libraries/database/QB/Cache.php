<?php
namespace Ars\Libraries\Database\QB;

trait Cache {

    /*
    |--------------------------------------------------------------------------
    | START CACHE
    |--------------------------------------------------------------------------
    */

    public function start_cache(){

        $this->qb_cache = true;

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | STOP CACHE
    |--------------------------------------------------------------------------
    */

    public function stop_cache(){

        $this->qb_cache = false;

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | FLUSH CACHE
    |--------------------------------------------------------------------------
    */

    public function flush_cache(){

        $this->qb_cache_data = [
            'select'   => [],
            'where'    => [],
            'join'     => [],
            'groupBy'  => [],
            'having'   => [],
            'order'    => [],
            'limit'    => '',
        ];

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | SAVE CACHE
    |--------------------------------------------------------------------------
    */

    protected function save_cache(
        $type,
        $value
    ){

        if (!$this->qb_cache) {
            return;
        }

        if (!array_key_exists(
            $type,
            $this->qb_cache_data
        )) {
            return;
        }

        /*
        |--------------------------------------------------------------------------
        | ARRAY CACHE
        |--------------------------------------------------------------------------
        */

        if (is_array(
            $this->qb_cache_data[$type]
        )) {

            $this->qb_cache_data[$type][] =
                $value;

            return;
        }

        /*
        |--------------------------------------------------------------------------
        | STRING CACHE
        |--------------------------------------------------------------------------
        */

        $this->qb_cache_data[$type] =
            $value;
    }

    /*
    |--------------------------------------------------------------------------
    | LOAD CACHE
    |--------------------------------------------------------------------------
    */

    protected function load_cache(){

        /*
        |--------------------------------------------------------------------------
        | SELECT
        |--------------------------------------------------------------------------
        */

        if (!empty(
            $this->qb_cache_data['select']
        )) {

            $this->select =
                array_merge(
                    $this->select,
                    $this->qb_cache_data['select']
                );
        }

        /*
        |--------------------------------------------------------------------------
        | WHERE
        |--------------------------------------------------------------------------
        */

        if (!empty(
            $this->qb_cache_data['where']
        )) {

            $this->where =
                array_merge(
                    $this->where,
                    $this->qb_cache_data['where']
                );
        }

        /*
        |--------------------------------------------------------------------------
        | JOIN
        |--------------------------------------------------------------------------
        */

        if (!empty(
            $this->qb_cache_data['join']
        )) {

            $this->join =
                array_merge(
                    $this->join,
                    $this->qb_cache_data['join']
                );
        }

        /*
        |--------------------------------------------------------------------------
        | GROUP BY
        |--------------------------------------------------------------------------
        */

        if (!empty(
            $this->qb_cache_data['groupBy']
        )) {

            $this->groupBy =
                array_merge(
                    $this->groupBy,
                    $this->qb_cache_data['groupBy']
                );
        }

        /*
        |--------------------------------------------------------------------------
        | HAVING
        |--------------------------------------------------------------------------
        */

        if (!empty(
            $this->qb_cache_data['having']
        )) {

            $this->having =
                array_merge(
                    $this->having,
                    $this->qb_cache_data['having']
                );
        }

        /*
        |--------------------------------------------------------------------------
        | ORDER
        |--------------------------------------------------------------------------
        */

        if (!empty(
            $this->qb_cache_data['order']
        )) {

            $this->order =
                $this->qb_cache_data['order'];
        }

        /*
        |--------------------------------------------------------------------------
        | LIMIT
        |--------------------------------------------------------------------------
        */

        if (!empty(
            $this->qb_cache_data['limit']
        )) {

            $this->limit =
                $this->qb_cache_data['limit'];
        }
    }

}