<?php
namespace Ars\Libraries\Database\QB;

trait Write {

    /*
    |--------------------------------------------------------------------------
    | SET
    |--------------------------------------------------------------------------
    */

    public function set(
        $key,
        $value = '',
        $escape = null
    ){

        /*
        |--------------------------------------------------------------------------
        | ARRAY SUPPORT
        |--------------------------------------------------------------------------
        */

        if (is_array($key)) {

            /*
            |--------------------------------------------------------------------------
            | BATCH [[]]
            |--------------------------------------------------------------------------
            */

            if (
                isset($key[0])
                && is_array($key[0])
            ) {

                $this->set = $key;

                return $this;
            }

            /*
            |--------------------------------------------------------------------------
            | SINGLE []
            |--------------------------------------------------------------------------
            */

            $this->set = $key;

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | SINGLE KEY VALUE
        |--------------------------------------------------------------------------
        */

        $this->set[$key] = $value;

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | INSERT
    |--------------------------------------------------------------------------
    */

    public function insert(
        $table = '',
        $set = null
    ){

        /*
        |--------------------------------------------------------------------------
        | TABLE
        |--------------------------------------------------------------------------
        */

        $table =
            $table ?: $this->table;

        /*
        |--------------------------------------------------------------------------
        | SET DATA
        |--------------------------------------------------------------------------
        */

        if ($set !== null) {

            $this->set($set);
        }

        /*
        |--------------------------------------------------------------------------
        | COMPILE SQL
        |--------------------------------------------------------------------------
        */

        $sql =
            $this->get_compiled_insert(
                $table
            );

        /*
        |--------------------------------------------------------------------------
        | EXECUTE
        |--------------------------------------------------------------------------
        */

        return $this->run($sql);
    }

    /*
    |--------------------------------------------------------------------------
    | UPDATE
    |--------------------------------------------------------------------------
    */

    public function update(
        $table = '',
        $set = [],
        $where = []
    ){

        /*
        |--------------------------------------------------------------------------
        | TABLE
        |--------------------------------------------------------------------------
        */

        $table =
            $table ?: $this->table;

        /*
        |--------------------------------------------------------------------------
        | SET DATA
        |--------------------------------------------------------------------------
        */

        if (!empty($set)) {

            $this->set($set);
        }

        /*
        |--------------------------------------------------------------------------
        | WHERE
        |--------------------------------------------------------------------------
        */

        if (!empty($where)) {

            $this->where($where);
        }

        /*
        |--------------------------------------------------------------------------
        | COMPILE SQL
        |--------------------------------------------------------------------------
        */

        $sql =
            $this->get_compiled_update(
                $table
            );

        /*
        |--------------------------------------------------------------------------
        | EXECUTE
        |--------------------------------------------------------------------------
        */

        return $this->run($sql);
    }
    public function update_batch($index = "",$table = "",$set = []) {
        if (empty($index)) {
        throw new \Exception('Index is require');
        }
        /*
        |--------------------------------------------------------------------------
        | TABLE
        |--------------------------------------------------------------------------
        */
    
        $table = $table ?: $this->table;
    
        /*
        |--------------------------------------------------------------------------
        | SET DATA
        |--------------------------------------------------------------------------
        */
    
        if (!empty($set)) {

            $this->set($set);
        }
    
        /*
        |--------------------------------------------------------------------------
        | COMPILE
        |--------------------------------------------------------------------------
        */
    
        $sql = $this->get_compiled_update_batch(
            $table,
            $index
        );
    
        /*
        |--------------------------------------------------------------------------
        | EXECUTE
        |--------------------------------------------------------------------------
        */
    
        return $this->run($sql);
    }
    /*
    |--------------------------------------------------------------------------
    | DELETE
    |--------------------------------------------------------------------------
    */

    public function delete(
        $table = '',
        $where = null
    ){

        /*
        |--------------------------------------------------------------------------
        | TABLE
        |--------------------------------------------------------------------------
        */

        $table =
            $table ?: $this->table;

        /*
        |--------------------------------------------------------------------------
        | WHERE
        |--------------------------------------------------------------------------
        */

        if ($where !== null) {

            $this->where($where);
        }

        /*
        |--------------------------------------------------------------------------
        | COMPILE SQL
        |--------------------------------------------------------------------------
        */

        $sql =
            $this->get_compiled_delete(
                $table
            );

        /*
        |--------------------------------------------------------------------------
        | EXECUTE
        |--------------------------------------------------------------------------
        */

        return $this->run($sql);
    }

    /*
    |--------------------------------------------------------------------------
    | TRUNCATE
    |--------------------------------------------------------------------------
    */

    public function truncate(
        $table = ''
    ){

        /*
        |--------------------------------------------------------------------------
        | TABLE
        |--------------------------------------------------------------------------
        */

        $table =
            $table ?: $this->table;

        /*
        |--------------------------------------------------------------------------
        | COMPILE SQL
        |--------------------------------------------------------------------------
        */

        $sql =
            $this->get_compiled_truncate(
                $table
            );

        /*
        |--------------------------------------------------------------------------
        | EXECUTE
        |--------------------------------------------------------------------------
        */

        return $this->run($sql);
    }

    /*
    |--------------------------------------------------------------------------
    | EMPTY TABLE
    |--------------------------------------------------------------------------
    */

    public function empty_table(
        $table = ''
    ){

        /*
        |--------------------------------------------------------------------------
        | TABLE
        |--------------------------------------------------------------------------
        */

        $table =
            $table ?: $this->table;

        /*
        |--------------------------------------------------------------------------
        | COMPILE SQL
        |--------------------------------------------------------------------------
        */

        $sql =
            $this->get_compiled_empty_table(
                $table
            );

        /*
        |--------------------------------------------------------------------------
        | EXECUTE
        |--------------------------------------------------------------------------
        */

        return $this->run($sql);
    }

    /*
    |--------------------------------------------------------------------------
    | REPLACE
    |--------------------------------------------------------------------------
    */

    public function replace(
        $table = '',
        $set = null
    ){

        /*
        |--------------------------------------------------------------------------
        | TABLE
        |--------------------------------------------------------------------------
        */

        $table =
            $table ?: $this->table;

        /*
        |--------------------------------------------------------------------------
        | SET DATA
        |--------------------------------------------------------------------------
        */

        if ($set !== null) {

            $this->set($set);
        }

        /*
        |--------------------------------------------------------------------------
        | COMPILE SQL
        |--------------------------------------------------------------------------
        */

        $sql =
            $this->get_compiled_replace(
                $table
            );

        /*
        |--------------------------------------------------------------------------
        | EXECUTE
        |--------------------------------------------------------------------------
        */

        return $this->run($sql);
    }

    /*
    |--------------------------------------------------------------------------
    | RUN WRITE
    |--------------------------------------------------------------------------
    */

    protected function run(
        $sql
    ){

        try {

            /*
            |--------------------------------------------------------------------------
            | PREPARE
            |--------------------------------------------------------------------------
            */

            $this->query($sql);

            /*
            |--------------------------------------------------------------------------
            | AUTO BIND
            |--------------------------------------------------------------------------
            */

            foreach (
                $this->bindings
                as $key => $value
            ) {

                $this->bind(
                    $key,
                    $value
                );
            }

            /*
            |--------------------------------------------------------------------------
            | EXECUTE
            |--------------------------------------------------------------------------
            */

            return $this->execute();

        } finally {

            /*
            |--------------------------------------------------------------------------
            | RESET QB
            |--------------------------------------------------------------------------
            */

            $this->reset_qb();
        }
    }
}