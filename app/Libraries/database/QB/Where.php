<?php
namespace Ars\Libraries\Database\QB;

trait Where {

    /*
    |--------------------------------------------------------------------------
    | ADD WHERE
    |--------------------------------------------------------------------------
    */

    protected function addWhere(
        $condition,
        $boolean = 'AND'
    ){

        /*
        |--------------------------------------------------------------------------
        | FIRST CONDITION
        |--------------------------------------------------------------------------
        */

        if (empty($this->where)) {

            $this->where[] = $condition;

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | AFTER GROUP START
        |--------------------------------------------------------------------------
        */

        $last =
            end($this->where);

        if (
            $last === '('
            || str_ends_with(
                trim($last),
                '('
            )
        ) {

            $this->where[] = $condition;

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | NORMAL
        |--------------------------------------------------------------------------
        */

        $this->where[] =
            "{$boolean} {$condition}";

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | WHERE
    |--------------------------------------------------------------------------
    */

    public function where(
        $key = null,
        $value = null,
        $escape = null
    ){

        return $this->buildWhere(
            $key,
            $value,
            'AND',
            $escape
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR WHERE
    |--------------------------------------------------------------------------
    */

    public function or_where(
        $key = null,
        $value = null,
        $escape = null
    ){

        return $this->buildWhere(
            $key,
            $value,
            'OR',
            $escape
        );
    }

    /*
    |--------------------------------------------------------------------------
    | BUILD WHERE
    |--------------------------------------------------------------------------
    */

    private function buildWhere(
        $key,
        $value,
        $boolean,
        $escape
    ){

        /*
        |--------------------------------------------------------------------------
        | ARRAY SUPPORT
        |--------------------------------------------------------------------------
        */

        if (is_array($key)) {

            foreach ($key as $k => $v) {

                $this->buildWhere(
                    $k,
                    $v,
                    $boolean,
                    $escape
                );
            }

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | RAW CONDITION
        |--------------------------------------------------------------------------
        */

        if ($value === null) {

            $this->addWhere(
                $key,
                $boolean
            );

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | OPERATOR DETECT
        |--------------------------------------------------------------------------
        */

        $field =
            trim($key);

        $operator = '=';

        if (
            preg_match(
                '/^(.+?)\s*(>=|<=|!=|<>|>|<|LIKE)$/i',
                trim($key),
                $match
            )
        ) {

            $field =
                trim($match[1]);

            $operator =
                strtoupper(
                    trim($match[2])
                );
        }

        /*
        |--------------------------------------------------------------------------
        | FIELD
        |--------------------------------------------------------------------------
        */

        $fieldProtected =
            $this->protect_identifier(
                $field
            );

        /*
        |--------------------------------------------------------------------------
        | PARAM
        |--------------------------------------------------------------------------
        */

        $param =
            'where_' .
            preg_replace(
                '/[^a-zA-Z0-9_]/',
                '_',
                $field
            ) .
            count($this->bindings);

        /*
        |--------------------------------------------------------------------------
        | CONDITION
        |--------------------------------------------------------------------------
        */

        $condition =
            "{$fieldProtected} {$operator} :{$param}";

        $this->addWhere(
            $condition,
            $boolean
        );

        /*
        |--------------------------------------------------------------------------
        | BINDING
        |--------------------------------------------------------------------------
        */

        $this->bindings[$param] =
            $value;

        return $this;
    }

    /*
    |--------------------------------------------------------------------------
    | WHERE IN
    |--------------------------------------------------------------------------
    */

    public function where_in(
        $key = null,
        $values = null,
        $escape = null
    ){

        return $this->buildWhereIn(
            $key,
            $values,
            'AND',
            false
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR WHERE IN
    |--------------------------------------------------------------------------
    */

    public function or_where_in(
        $key = null,
        $values = null,
        $escape = null
    ){

        return $this->buildWhereIn(
            $key,
            $values,
            'OR',
            false
        );
    }

    /*
    |--------------------------------------------------------------------------
    | WHERE NOT IN
    |--------------------------------------------------------------------------
    */

    public function where_not_in(
        $key = null,
        $values = null,
        $escape = null
    ){

        return $this->buildWhereIn(
            $key,
            $values,
            'AND',
            true
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR WHERE NOT IN
    |--------------------------------------------------------------------------
    */

    public function or_where_not_in(
        $key = null,
        $values = null,
        $escape = null
    ){

        return $this->buildWhereIn(
            $key,
            $values,
            'OR',
            true
        );
    }

    /*
    |--------------------------------------------------------------------------
    | BUILD WHERE IN
    |--------------------------------------------------------------------------
    */

    private function buildWhereIn(
        $key,
        $values,
        $boolean,
        $not
    ){

        if (
            !is_array($values) ||
            empty($values)
        ) {

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | PARAMS
        |--------------------------------------------------------------------------
        */

        $params = [];

        foreach ($values as $i => $val) {

            $param =
                'wherein_' .
                preg_replace(
                    '/[^a-zA-Z0-9_]/',
                    '_',
                    $key
                ) .
                '_' .
                count($this->bindings) .
                "_{$i}";

            $params[] =
                ":{$param}";

            $this->bindings[$param] =
                $val;
        }

        /*
        |--------------------------------------------------------------------------
        | OPERATOR
        |--------------------------------------------------------------------------
        */

        $operator =
            $not
            ? 'NOT IN'
            : 'IN';

        /*
        |--------------------------------------------------------------------------
        | FIELD
        |--------------------------------------------------------------------------
        */

        $field =
            $this->protect_identifier(
                $key
            );

        /*
        |--------------------------------------------------------------------------
        | CONDITION
        |--------------------------------------------------------------------------
        */

        $condition =
            "{$field} {$operator} ("
            . implode(', ', $params)
            . ')';

        $this->addWhere(
            $condition,
            $boolean
        );

        return $this;
    }
}