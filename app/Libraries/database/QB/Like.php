<?php
namespace Ars\Libraries\Database\QB;
trait Like {

    /*
    |--------------------------------------------------------------------------
    | LIKE
    |--------------------------------------------------------------------------
    */

    public function like(
        $field,
        $match = '',
        $side = 'both',
        $escape = null
    ){

        return $this->buildLike(
            $field,
            $match,
            'AND',
            false,
            $side,
            $escape
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR LIKE
    |--------------------------------------------------------------------------
    */

    public function or_like(
        $field,
        $match = '',
        $side = 'both',
        $escape = null
    ){

        return $this->buildLike(
            $field,
            $match,
            'OR',
            false,
            $side,
            $escape
        );
    }

    /*
    |--------------------------------------------------------------------------
    | NOT LIKE
    |--------------------------------------------------------------------------
    */

    public function not_like(
        $field,
        $match = '',
        $side = 'both',
        $escape = null
    ){

        return $this->buildLike(
            $field,
            $match,
            'AND',
            true,
            $side,
            $escape
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR NOT LIKE
    |--------------------------------------------------------------------------
    */

    public function or_not_like(
        $field,
        $match = '',
        $side = 'both',
        $escape = null
    ){

        return $this->buildLike(
            $field,
            $match,
            'OR',
            true,
            $side,
            $escape
        );
    }

    /*
    |--------------------------------------------------------------------------
    | BUILD LIKE
    |--------------------------------------------------------------------------
    */

    private function buildLike(
        $field,
        $match,
        $boolean,
        $not,
        $side,
        $escape
    ){

        /*
        |--------------------------------------------------------------------------
        | ESCAPE INPUT
        |--------------------------------------------------------------------------
        */

        if ($escape !== false) {

            $match = str_replace(
                ['%', '_'],
                ['\\%', '\\_'],
                $match
            );
        }

        /*
        |--------------------------------------------------------------------------
        | WILDCARD SIDE
        |--------------------------------------------------------------------------
        */

        switch (strtolower($side)) {

            case 'before':

                $match = "%{$match}";
                break;

            case 'after':

                $match = "{$match}%";
                break;

            case 'none':

                break;

            default:

                $match = "%{$match}%";
                break;
        }

        /*
        |--------------------------------------------------------------------------
        | PARAM
        |--------------------------------------------------------------------------
        */

        $param =
            'like_' .
            str_replace(
                ['.', ' '],
                '_',
                $field
            ) .
            count($this->bindings);

        /*
        |--------------------------------------------------------------------------
        | OPERATOR
        |--------------------------------------------------------------------------
        */

        $operator =
            $not
            ? 'NOT LIKE'
            : 'LIKE';

        /*
        |--------------------------------------------------------------------------
        | CONDITION
        |--------------------------------------------------------------------------
        */

        $condition =
            "`{$field}` {$operator} :{$param}";

        /*
        |--------------------------------------------------------------------------
        | ADD WHERE
        |--------------------------------------------------------------------------
        */

        $this->addWhere(
            $condition,
            $boolean
        );

        /*
        |--------------------------------------------------------------------------
        | BIND
        |--------------------------------------------------------------------------
        */

        $this->bindings[$param] = $match;

        return $this;
    }

}