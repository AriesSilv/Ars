<?php
namespace Ars\Libraries\Database\QB;

trait Like {

    /*
    |--------------------------------------------------------------------------
    | LIKE
    |--------------------------------------------------------------------------
    */

    public function like(
        $field,
        $match = '',
        $side = 'both',
        $escape = null
    ){

        return $this->buildLike(
            $field,
            $match,
            'AND',
            false,
            $side
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR LIKE
    |--------------------------------------------------------------------------
    */

    public function or_like(
        $field,
        $match = '',
        $side = 'both',
        $escape = null
    ){

        return $this->buildLike(
            $field,
            $match,
            'OR',
            false,
            $side
        );
    }

    /*
    |--------------------------------------------------------------------------
    | NOT LIKE
    |--------------------------------------------------------------------------
    */

    public function not_like(
        $field,
        $match = '',
        $side = 'both',
        $escape = null
    ){

        return $this->buildLike(
            $field,
            $match,
            'AND',
            true,
            $side
        );
    }

    /*
    |--------------------------------------------------------------------------
    | OR NOT LIKE
    |--------------------------------------------------------------------------
    */

    public function or_not_like(
        $field,
        $match = '',
        $side = 'both',
        $escape = null
    ){

        return $this->buildLike(
            $field,
            $match,
            'OR',
            true,
            $side
        );
    }

    /*
    |--------------------------------------------------------------------------
    | BUILD LIKE
    |--------------------------------------------------------------------------
    */

    private function buildLike(
        $field,
        $match,
        $boolean,
        $not,
        $side
    ){

        /*
        |--------------------------------------------------------------------------
        | ARRAY SUPPORT
        |--------------------------------------------------------------------------
        */

        if (is_array($field)) {

            foreach ($field as $k => $v) {

                $this->buildLike(
                    $k,
                    $v,
                    $boolean,
                    $not,
                    $side
                );
            }

            return $this;
        }

        /*
        |--------------------------------------------------------------------------
        | ESCAPE LIKE
        |--------------------------------------------------------------------------
        */

        $match = str_replace(
            ['%', '_'],
            ['\\%', '\\_'],
            $match
        );

        /*
        |--------------------------------------------------------------------------
        | SIDE
        |--------------------------------------------------------------------------
        */

        switch (strtolower($side)) {

            case 'before':

                $match = "%{$match}";
                break;

            case 'after':

                $match = "{$match}%";
                break;

            case 'none':

                break;

            default:

                $match = "%{$match}%";
                break;
        }

        /*
        |--------------------------------------------------------------------------
        | PARAM
        |--------------------------------------------------------------------------
        */

        $param =
            'like_' .
            preg_replace(
                '/[^a-zA-Z0-9_]/',
                '_',
                $field
            ) .
            count($this->bindings);

        /*
        |--------------------------------------------------------------------------
        | OPERATOR
        |--------------------------------------------------------------------------
        */

        $operator =
            $not
            ? 'NOT LIKE'
            : 'LIKE';

        /*
        |--------------------------------------------------------------------------
        | FIELD
        |--------------------------------------------------------------------------
        */

        $field =
            $this->protect_identifier($field);

        /*
        |--------------------------------------------------------------------------
        | CONDITION
        |--------------------------------------------------------------------------
        */

        $condition =
            "{$field} {$operator} :{$param}";

        $this->addWhere(
            $condition,
            $boolean
        );

        /*
        |--------------------------------------------------------------------------
        | BIND
        |--------------------------------------------------------------------------
        */

        $this->bindings[$param] = $match;

        return $this;
    }

}